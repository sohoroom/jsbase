var x = 3;
console.log('x = '+ x)

switch(x) {
  case 1:
    console.log('x = 1');
    break;
  case 2:
    console.log('x = 2');
    break;
  case 3:
    console.log('x = 3');
    break;
  case 4:
    console.log('x = 4');
    break;
  case 5:
    console.log('x = 5');
    break;
  default:
    console.log('x值不為1~5任一數')
}